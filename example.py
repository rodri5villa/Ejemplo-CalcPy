import CalcPy as cp

# Operaciones básicas
print("Suma (10 + 5):", cp.suma(10, 5))
print("Resta (10 - 5):", cp.resta(10, 5))

# Operaciones científicas
print("Seno (pi/2):", cp.seno(3.141592653589793 / 2))
print("Coseno (pi):", cp.coseno(3.141592653589793))

# Operaciones avanzadas
print("Factorial (5):", cp.factorial(5))
